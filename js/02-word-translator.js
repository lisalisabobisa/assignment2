

let langCode = window.prompt("Enter your language code");

if (langCode === "es") {
    document.write("Hola!");
} else if (langCode === "de") {
    document.write("Hallo");
} else if (langCode === "en") {
    document.write("Hello!");
} else if (langCode === "fr") {
    document.write("Bojour le monde");
}